//Example on class with Compute Methods

public class Maths
{
	public int add(int x,int y){
		return x+y;
	}

	public int sub(int x,int y){
			return x-y;
	}

	public int product(int x,int y){
			return x*y;
	}

	public int mod(int x,int y){
			return x%y;
	}

	public int division(int x,int y){
			return x/y;
	}
}
